    private void EmpAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpAgregarActionPerformed
        // TODO add your handling code here:
        String dpi,nom1,ape1,nom2,ape2,tel,cel,dire,hentrada,hsalida,igss,salario,puesto;
        String sql= "";
        dpi=DPIEmp.getText();
        nom1=nombre1Emp.getText();
        nom2=nombre2Emp.getText();
        ape1=apellido1Emp.getText();
        ape2=apellido2Emp.getText();
        hentrada=horaEntradaEmp.getText();
        hsalida=horaSalidaEmp.getText();
        igss=igssEmp.getText();
        salario=salarioEmp.getText();
        dire=direccionEmp.getText();
        cel=celularEmp.getText();
        tel=telefonoEmp.getText();
        puesto=puestoEmp.getText();

        sql = "INSERT INTO trabajador(dpi,primer_nombre,segundo_nombre,primer_apelido,segundo_apellido,igss,salario,puesto,direccion,celular,telefono) VALUES(?,?,?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,dpi);
            pst.setString(2,nom1);
            pst.setString(3,nom2);
            pst.setString(4,ape1);
            pst.setString(5,ape2);
            //pst.setString(6,hentrada);
            //pst.setString(7,hsalida);
            pst.setString(6,igss);
            pst.setString(7,salario);
            pst.setString(8,puesto);
            pst.setString(9,dire);
            pst.setString(10,cel);
            pst.setString(11,tel);
            int n = pst.executeUpdate();
            if (n > 0 ){
                JOptionPane.showMessageDialog(null,"Registro guardado!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Error al Ingresar");
        }
    }//GEN-LAST:event_EmpAgregarActionPerformed

